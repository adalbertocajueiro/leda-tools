package br.edu.ufcg.ccc.leda.submission.util;

public class RoteiroException extends Exception {

	public RoteiroException() {
		super();
	}

	public RoteiroException(String message) {
		super(message);
	}

	public RoteiroException(Throwable cause) {
		super(cause);
	}

	public RoteiroException(String message, Throwable cause) {
		super(message, cause);
	}

	public RoteiroException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
